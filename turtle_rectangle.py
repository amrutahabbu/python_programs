'''
Write a program that prompts the user to enter the
center of a rectangle, width, and height, and displays the rectangle
'''

import turtle

turtle.showturtle()

turtle.penup()
turtle.goto(-70,50)
turtle.pendown()
for i in range(0,2):
    turtle.forward(140)
    turtle.right(90)
    turtle.forward(100)
    turtle.right(90)


turtle.done()